#include "push_swap.h"

void	push_a(int *a, int *b, int *nb_a, int *nb_b)
{
	int	i;

	i = -1;
	while (++i < *nb_b)
		b[*nb_b - i] = b[*nb_b - i - 1];
	b[0] = a[0];
	(*nb_b)++;
	(*nb_a)--;
	i = -1;
	while (++i < *nb_a)
		a[i] = a[i + 1];
}
